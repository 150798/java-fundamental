package tugas5;

public class Struck {
	String menu;
	int jumlah;
	double harga;
	
	public Struck(String menu, int jumlah, double harga) {
		super();
		this.menu = menu;
		this.jumlah = jumlah;
		this.harga = harga;
	}

	public String getMenu() {
		return menu;
	}

	public void setMenu(String menu) {
		this.menu = menu;
	}

	public int getJumlah() {
		return jumlah;
	}

	public void setJumlah(int jumlah) {
		this.jumlah = jumlah;
	}

	public double getHarga() {
		return harga;
	}

	public void setHarga(double harga) {
		this.harga = harga;
	}
	
	

}
